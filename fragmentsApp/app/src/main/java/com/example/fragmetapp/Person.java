package com.example.fragmetapp;

public class Person {
    public String email;
    public String password;
    public String phone;
    public String name;

    public Person() {}

    public Person(String email, String password, String phone, String name) {
        this.email = email;
        this.password = password;
        this.phone = phone;
        this.name = name;
    }
}
